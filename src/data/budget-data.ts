// Grup Konsolide = ICA + ICE toplamı
export const GROUP_MONTHLY: MonthlyEntry[] = ICA_BUDGET.monthlyData.map((icaRow, i) => {
  const iceRow = ICE_BUDGET.monthlyData[i];
  const merged: MonthlyEntry = { month: icaRow.month, monthLabel: icaRow.monthLabel };
  for (const key of Object.keys(icaRow)) {
    if (key === 'month' || key === 'monthLabel') continue;
    merged[key] = (icaRow[key] as number) + (iceRow[key] as number);
  }
  return merged;
});
