// ─── builder ─────────────────────────────────────────────────────────────────

function buildMonthlyData(raw: Record<string, number[]>): MonthlyEntry[] {
  return MONTHS.map(({ key, label }, i) => {
    const entry: MonthlyEntry = { month: key, monthLabel: label };
    for (const [cat, values] of Object.entries(raw)) {
      entry[cat] = values[i];
    }
    return entry;
  });
}

